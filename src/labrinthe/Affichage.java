    public boolean deplacerJoueur(Cellule uneCellule) {
        /*
        Prends la cellule sur laquelle à cliqué le joueur et déplace le joueur 
        dessus si c'est possible, renvoie true dans ce cas, si les déplacement 
        ne s'est pas fait on renvoie false
         */
        int x = 0;//on retrouve les coordonées de la cellule du joueurCourant
        int y = 0;
        for (int i = 0; i < 7; i++) {
            for (int j = 0; j < 7; j++) {
                if (grilleJeu.cellule[i][j] != null && grilleJeu.cellule[i][j].pionCourant == joueurCourant) {
                    x = i;
                    y = j;
                }
            }
        }
        // on recherche les cellules sur lesquelles le pion peut se déplacer 
        //et on change leur variable 'deplacement' sur true si c'est le cas 
        grilleJeu.rechercheCelluleDeplacement(grilleJeu.cellule[x][y]);

        // au cas ou le joueur décide de ne pas bouger ou qu'il ne peut pas 
        //bouger on passe sa case comme valable pour un déplacement
        grilleJeu.cellule[x][y].deplacement = true;

        // si la case choisie est une case sur laquelle on peut se deplacer 
        //alors on deplace et renvoie true, sinon on renvoie false
        if (uneCellule.deplacement == true) {
            uneCellule.affecterJoueur(grilleJeu.cellule[x][y].repcupererPion());
            grilleJeu.reinitialisationDeplacement();
            tourDeJeu = false;
            return true;
        }

        // une foi le pion déplacé on remet à false les variables 'deplacement' 
        grilleJeu.reinitialisationDeplacement();
        return false;

    }

    public boolean trouverTresor(Cellule uneCellule) {
        if (joueurCourant.tresor1 == uneCellule.tresor || joueurCourant.tresor2 == uneCellule.tresor) {
            return true;
        }
        return false;
    }

    public void prochainJoueur() {
        switch (nbJoueurs) {
            case 1:
                break;
            case 2:
                if (joueurCourant == listeJoueurs[0]) {
                    joueurCourant = listeJoueurs[1];
                } else {
                    joueurCourant = listeJoueurs[0];
                }
                break;
            case 3:
                if (joueurCourant == listeJoueurs[0]) {
                    joueurCourant = listeJoueurs[1];
                } else if (joueurCourant == listeJoueurs[1]) {
                    joueurCourant = listeJoueurs[2];
                } else if (joueurCourant == listeJoueurs[2]) {
                    joueurCourant = listeJoueurs[0];
                }
            case 4:
                if (joueurCourant == listeJoueurs[0]) {
                    joueurCourant = listeJoueurs[1];
                } else if (joueurCourant == listeJoueurs[1]) {
                    joueurCourant = listeJoueurs[2];
                } else if (joueurCourant == listeJoueurs[2]) {
                    joueurCourant = listeJoueurs[3];
                } else if (joueurCourant == listeJoueurs[3]) {
                    joueurCourant = listeJoueurs[0];
                }
        }
    }

    public void initialiserPartie() {
        /* on place les cellules immobiles
         des angles dans les 4 coins et dans les cellules  : 
        (0,2) (0,4)
        (2,0) (2,2) (2,4) (2,6) 
        (4,0) (4,2) (4,4) (4,6)
        (6,2) (6,4) 
        des 'T' pointant vers le centre pour ceux sur les bords 
         */

        //placement des angles
        grilleJeu.cellule[0][0].angle(0);
        grilleJeu.cellule[6][0].angle(1);
        grilleJeu.cellule[6][6].angle(2);
        grilleJeu.cellule[0][6].angle(3);

        //placement des bords
        grilleJeu.cellule[0][2].t(0);
        grilleJeu.cellule[0][4].t(0);
        grilleJeu.cellule[2][0].t(1);
        grilleJeu.cellule[4][0].t(1);
        grilleJeu.cellule[2][6].t(2);
        grilleJeu.cellule[4][6].t(2);
        grilleJeu.cellule[6][2].t(3);
        grilleJeu.cellule[6][4].t(3);

        //placement des cellules internes
        grilleJeu.cellule[2][2].t(0);
        grilleJeu.cellule[4][2].t(1);
        grilleJeu.cellule[2][4].t(2);
        grilleJeu.cellule[4][4].t(3);
        
        //on créée un tableau contenant toutes les autres cases à placer 
        Cellule tabCellule[] = new Cellule[34];

        for (int i = 0; i < 34; i++) {  // on associe cellules en bon nombre et forme 
            if (i < 15) {
                tabCellule[i] = new Cellule(0, i); //15 angles
            } else if (i > 14 && i < 19) {
                tabCellule[i] = new Cellule(1, (i % 14)); // 4 't'
            } else if (i > 18 && i < 32) {
                tabCellule[i] = new Cellule(2, (i % 4)); //13 lignes
            } else {
                tabCellule[i] = new Cellule(3, 0); // 2 croix
            }
            System.out.print(tabCellule[i].type);
        }
        

        //on mélange ce tableau 
        //melangerTableau(tabCellule);

        // on intègre les cases de ce tableau aux cellules vides de la grille;
        int x = 0; // variable permettant de se déplacer dans tabCellules
        while (x < 33) {
            for (int i = 0; i < 7; i++) {
                for (int j = 0; j < 7; j++) {
                    if (grilleJeu.cellule[i][j].type == -1) {
                        grilleJeu.cellule[i][j] = tabCellule[x];
                        System.out.println(x);
                        System.out.println(tabCellule[x].type);
                        x++;
                    }
                }
            }
        }
        grilleJeu.celluleExt = tabCellule[33]; // stock la cellule ext
        PJeu.repaint();
    }

    public static Cellule[] melangerTableau(Cellule[] tab) {
        // cette methode permet de mélanger la tableau des cellules afin que chaque 
        Random rgen = new Random();  // Generateur de nombre aléatoire		

        for (int i = 0; i < tab.length; i++) {
            int randomPosition = rgen.nextInt(tab.length);
            Cellule temp = tab[i];
            tab[i] = tab[randomPosition];
            tab[randomPosition] = temp;
        }

        return tab;
    }

